    routers_dir = "routers"
    fixed_count = 0
    
    if not os.path.exists(routers_dir):
        print(f"‚ùå Directory {routers_dir} not found")
        return
    
    print("üîß Fixing imports in router files...")
    print("=" * 50)
    
    for filename in os.listdir(routers_dir):
        if filename.endswith('.py') and filename != '__init__.py':
            file_path = os.path.join(routers_dir, filename)
            if fix_imports_in_file(file_path):
                fixed_count += 1
    
    # ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡πÑ‡∏ü‡∏•‡πå‡∏≠‡∏∑‡πà‡∏ô‡πÜ ‡∏ó‡∏µ‡πà‡∏≠‡∏≤‡∏à‡∏°‡∏µ‡∏õ‡∏±‡∏ç‡∏´‡∏≤

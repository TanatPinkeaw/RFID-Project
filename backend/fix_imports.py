    print("\nüîß Fixing imports in other files...")
    for filename in other_files:
        if os.path.exists(filename):
            if fix_imports_in_file(filename):
                fixed_count += 1
    
    print("=" * 50)
    print(f"üéâ Fixed imports in {fixed_count} files")

